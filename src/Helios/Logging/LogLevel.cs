namespace Helios.Logging
{
    /// <summary>
    /// Enumeration representing the various log levels in the system.
    /// </summary>
    public enum LogLevel
    {
        /// <summary>
        /// The debug log level.
        /// </summary>
        Debug,

        /// <summary>
        /// The information log level.
        /// </summary>
        Info,

        /// <summary>
        /// The warning log level.
        /// </summary>
        Warning,

        /// <summary>
        /// The error log level.
        /// </summary>
        Error,
    }
}